#include "libft.h"

char	*ft_strjoin(char *s1, const char *s2)
{
	char	*join;
	int		i;
	int		j;

	if (!s1)
		return (NULL);
	if (!s2)
		s2 = "";
	join = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (join == NULL)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != '\0')
		join[i++] = s1[j++];
	j = 0;
	while (s2[j] != '\0')
		join[i++] = s2[j++];
	join[i] = '\0';
	return (join);
}
